import java.util.*;
public class Mul 
{
    public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter any number :");
        int n = sc.nextInt();
        for(int i=0 ; i<= 10 ; i++){
            System.out.println(n + " * " + i + " = " + n*i);
            System.out.println();
        }
    }
    }
