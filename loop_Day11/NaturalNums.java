import java.util.Scanner;
public class NaturalNums
  {
    public static void main(String[] args){
      Scanner sc = new Scanner(System.in);
      System.out.println("enter number n :");
      int n = sc.nextInt();
      int i = 1;
      while(i<=n){
        for( i = 1;i<=n;i++){
          System.out.println(i);
          
        }
      }
       
    }
  }